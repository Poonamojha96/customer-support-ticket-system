package com.practice.api.model;

/**
 * Enum representing the status of a ticket.
 */
public enum Status {

    /**
     * Indicates that the ticket is new and has not yet been processed.
     */
    NEW,

    /**
     * Indicates that the ticket is currently being worked on.
     */
    INPROGRESS,

    /**
     * Indicates that the ticket has been completed.
     */
    COMPLETED
}
